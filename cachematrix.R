## makeCacheMatrix function creates r object that will store matrix and its inverse
## cacheSolve function will retrieve cached inverse value of a matrix from makeCacheMatix function


## This function takes x as a matrix, get its inverse, and stores it as a chaced data.

makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinverse <- function(solve) m <<- solve
  getinverse <- function() m
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)

}


## calcuates inverse of a matrix if it is not already in the cached data

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  m <- x$getinverse()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setinverse(m)
  m
}
